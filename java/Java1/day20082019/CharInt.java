class CharInt 
{
	public static void main(String[] args) 
	{
		char c='a';
		int i=10;

		int r=c+i;
		System.out.println(r);//Addition

		r=c-i;
		System.out.println(r);//Subtraction

		r=c*i;
		System.out.println(r);//Multiplication

		r=c/i;
		System.out.println(r);//Division

		r=c%i;
		System.out.println(r);//Modulo-Divisionf
	}
}
